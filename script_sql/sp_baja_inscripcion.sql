CREATE PROCEDURE [dbo].[SP_BAJA_INSCRIPCION]
  @ID				INT
AS


BEGIN TRAN 
	
	DELETE FROM INSCRIPCION
	WHERE ID = @ID
		

	IF @@ERROR <> 0
	BEGIN
		RAISERROR('PROBLEMAS AL ELIMINAR INSCRIPCION',16,1)
		ROLLBACK
		RETURN 99
	END

COMMIT
GO


